void main() {
  // + , - , / , *, %

  int a = 100;
  int b = 20;

  //Addition
  int r = a + b;
  print(r);

  r = b - a;
  print(r);

  r = b * a;
  print(r);

  r = a % 3;
  print(r);

  double r1 = a / 3;
  print(r1);
}


